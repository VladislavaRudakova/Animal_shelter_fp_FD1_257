class Pet {

constructor(id, pet, name, age, sex, character= [], 
    avatarPhoto, avatarBackPhoto, accPhotos=[], 
    textInfo, color=[], furType, furLenght) {
    this.id=id;
    this.pet=pet;
    this.name=name;
    this.age=age;
    this.sex=sex;
    this.character=character;
    this.avatarPhoto=avatarPhoto;
    this.avatarBackPhoto=avatarBackPhoto;
    this.accPhotos=accPhotos;
    this.textInfo=textInfo;
    this.color=color;
    this.furType=furType;
    this.furLenght=furLenght;

}

}

